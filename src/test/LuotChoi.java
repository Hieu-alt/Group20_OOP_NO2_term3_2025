public class LuotChoi {
    private String tenNguoiChoi;
    private int diem;

    public LuotChoi(String tenNguoiChoi, int diem) {
        this.tenNguoiChoi = tenNguoiChoi;
        this.diem = diem;
    }

    public String getTenNguoiChoi() {
        return tenNguoiChoi;
    }

    public int getDiem() {
        return diem;
    }
}